package com.itcrowd.book.model;

import java.util.Date;

public class BookItemAuthorsVO {

	private int authorId;
	private String authorName;
	private String authorType;
	private String authorTypeDesc;
	private String authorInfo;
	private String authorInfoLink;
	public int getAuthorId() {
		return authorId;
	}
	public void setAuthorId(int authorId) {
		this.authorId = authorId;
	}
	public String getAuthorName() {
		return authorName;
	}
	public void setAuthorName(String authorName) {
		this.authorName = authorName;
	}
	public String getAuthorType() {
		return authorType;
	}
	public void setAuthorType(String authorType) {
		this.authorType = authorType;
	}
	public String getAuthorTypeDesc() {
		return authorTypeDesc;
	}
	public void setAuthorTypeDesc(String authorTypeDesc) {
		this.authorTypeDesc = authorTypeDesc;
	}
	public String getAuthorInfo() {
		return authorInfo;
	}
	public void setAuthorInfo(String authorInfo) {
		this.authorInfo = authorInfo;
	}
	public String getAuthorInfoLink() {
		return authorInfoLink;
	}
	public void setAuthorInfoLink(String authorInfoLink) {
		this.authorInfoLink = authorInfoLink;
	}
	
	
}
